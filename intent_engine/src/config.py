def _parse_ats_boards(env_val: str) -> list[dict]:
    boards = []
    for entry in env_val.split(","):
        entry = entry.strip()
        if ":" in entry:
            ats, token = entry.split(":", 1)
            if ats.strip() and token.strip():
                boards.append({"ats": ats.strip(), "token": token.strip()})
    return boards

