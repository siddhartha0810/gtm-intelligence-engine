"""
config.py  (intent_engine)
==================================
Central configuration loader for the intent detection engine.

PURPOSE:
  Single source of truth for all API keys, database connection details, rate-limit
  settings, and the seed search queries that drive the job-board scrapers.
  All secrets come from intent_engine/.env — never hardcoded.

  The engine is taxonomy-driven, not tied to one vendor: the active product/phase
  taxonomy is loaded at runtime from the `product_taxonomy` DB table (falling back
  to icp_profiles/*.yaml — see phase_classifier.py). QUERIES_BY_PRODUCT below is
  handcrafted seed data for products in the default taxonomy; any product added to
  the taxonomy without a handcrafted entry gets queries auto-generated by
  generate_queries_for_product(). Individual campaigns (e.g. icp_profiles/quadsci.yaml,
  endex.yaml) supply their own keywords entirely and don't touch this file.

HOW IT FITS IN THE SYSTEM:
  Imported at the top of almost every intent_engine module.
  unified_app.py loads the .env file BEFORE importing this module, so any env vars
  set by unified_app.py are already visible when config attributes are first read.

KEY ATTRIBUTES:
  DB_*                  — PostgreSQL connection details
  APOLLO_API_KEY        — used by apollo_enrichment.py (X-Api-Key header, NOT Bearer)
  ZEROBOUNCE_API_KEY    — used by apollo_enrichment.py for email validation
  QUERIES_BY_PRODUCT    — handcrafted job-board queries for the default taxonomy's products
  JDE_MANUFACTURING_QUERIES — extra queries for the JDE Manufacturing Focus mode
  NEWS_QUERIES          — queries consumed by news_signal.py
  MAX_PAGES             — per-source page cap (prevents runaway scraping costs)
  SCAN_DELAY_MIN/MAX    — seconds to sleep between HTTP requests (politeness tier)

DEPENDENCIES:
  - python-dotenv (reads intent_engine/.env)
  - No DB or API calls occur at import time
"""

